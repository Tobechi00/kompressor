    uint8_t convertChunkToByte(const std::string &chunk){
        int ans = 0;

        for(int i = 0; i < 8; i++){
            int bit = chunk[i] - '0';
            ans += std::pow(2, 7 - i) * bit;
        }

        return static_cast<uint8_t>(ans);
    }



